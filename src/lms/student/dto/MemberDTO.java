package lms.student.dto;


public class MemberDTO {
	
	private String memberId;
	private String mName;
	private String mPwd;
	private String mEmail;
	private String mBirth;
	private String mGender;
	private int mZipcode;
	private String mAddr1;
	private String member_div;
	private String mPhone;
	private String mTel;
	
	
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getmPwd() {
		return mPwd;
	}
	public void setmPwd(String mPwd) {
		this.mPwd = mPwd;
	}
	public String getmEmail() {
		return mEmail;
	}
	public void setmEmail(String mEmail) {
		this.mEmail = mEmail;
	}
	public String getmBirth() {
		return mBirth;
	}
	public void setmBirth(String mBirth) {
		this.mBirth = mBirth;
	}
	public String getmGender() {
		return mGender;
	}
	public void setmGender(String mGender) {
		this.mGender = mGender;
	}
	public int getmZipcode() {
		return mZipcode;
	}
	public void setmZipcode(int mZipcode) {
		this.mZipcode = mZipcode;
	}
	public String getmAddr1() {
		return mAddr1;
	}
	public void setmAddr1(String mAddr1) {
		this.mAddr1 = mAddr1;
	}
	public String getMember_div() {
		return member_div;
	}
	public void setMember_div(String member_div) {
		this.member_div = member_div;
	}
	public String getmPhone() {
		return mPhone;
	}
	public void setmPhone(String mPhone) {
		this.mPhone = mPhone;
	}
	public String getmTel() {
		return mTel;
	}
	public void setmTel(String mTel) {
		this.mTel = mTel;
	}
	
}
